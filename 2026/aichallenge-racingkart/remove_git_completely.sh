#!/bin/bash
# ==============================================================================
# remove_git_completely.sh
#
# 【このスクリプトの考え方】
#
#   指定フォルダが git 管理下かを判定し、git/GitHub に関連する
#   ファイル・ディレクトリをすべて削除して「普通のフォルダ」に戻す。
#
#   ソースファイルや通常ファイルは一切削除しない。
#   削除後は git コマンドが一切効かない状態になる。
#
#   処理後の状態:
#     「git init していない普通のフォルダ」になる。
#     git status を実行すると "not a git repository" と表示される。
#
# 【削除対象】
#
#   指定フォルダ直下:
#     .git/          git の全管理情報（履歴・設定・リモート情報すべて）
#     .github/       GitHub Actions / Issue テンプレート等の設定フォルダ
#     .gitignore     git の無視ファイル設定
#     .gitattributes git の属性設定ファイル
#     .gitmodules    サブモジュール設定ファイル
#
#   サブフォルダ配下（再帰検索）:
#     上記と同名のファイル・フォルダを find で再帰的に削除する。
#     ネストされたサブモジュール等も対象になる。
#
# 【削除しないもの】
#   ソースコード・ドキュメント等、通常のワーキングツリーファイル一切
#
# 【使い方】
#   bash remove_git_completely.sh              # カレントディレクトリを対象
#   bash remove_git_completely.sh /path/to/repo
#
# ==============================================================================

set -euo pipefail

# ------------------------------------------------------------------------------
# 引数処理：対象ディレクトリの決定
# ------------------------------------------------------------------------------
TARGET_DIR="${1:-.}"
TARGET_DIR="$(realpath "$TARGET_DIR")"

echo "========================================"
echo " 対象ディレクトリ: $TARGET_DIR"
echo "========================================"

# ------------------------------------------------------------------------------
# STEP 1: git 管理フォルダかどうかを判定
# ------------------------------------------------------------------------------
# git rev-parse --git-dir は .git ディレクトリのパスを返す。
# git 管理外なら終了ステータス非ゼロになるため、それで判定する。
if git -C "$TARGET_DIR" rev-parse --git-dir > /dev/null 2>&1; then
    GIT_DIR="$(git -C "$TARGET_DIR" rev-parse --absolute-git-dir)"
    echo "[OK] git 管理フォルダです。"
    echo "     .git の場所: $GIT_DIR"
else
    echo "[NG] このフォルダは git 管理下ではありません。処理を終了します。"
    exit 1
fi

# ------------------------------------------------------------------------------
# STEP 2: 削除対象のプレビューを表示
# ------------------------------------------------------------------------------
echo ""
echo "--- 削除対象プレビュー（find 結果）---"

# 削除対象パターンを配列で定義
#   .git       : git の全管理情報
#   .github    : GitHub Actions / テンプレート等
#   .gitignore : 無視ファイル設定
#   .gitattributes : 属性設定
#   .gitmodules    : サブモジュール設定
PATTERNS=(".git" ".github" ".gitignore" ".gitattributes" ".gitmodules")

FOUND_ANY=false
for pattern in "${PATTERNS[@]}"; do
    # -maxdepth を付けないことでサブフォルダも再帰検索する
    while IFS= read -r found; do
        echo "  $found"
        FOUND_ANY=true
    done < <(find "$TARGET_DIR" -name "$pattern" 2>/dev/null)
done

if [ "$FOUND_ANY" = false ]; then
    echo "  (削除対象が見つかりませんでした)"
fi
echo "--------------------------------------"

# ------------------------------------------------------------------------------
# STEP 3: 削除前の確認プロンプト
# ------------------------------------------------------------------------------
echo ""
echo "【警告】上記をすべて削除します。この操作は取り消せません。"
echo "        ソースファイルは削除されませんが、git 履歴はすべて失われます。"
echo ""
read -rp "本当に続行しますか？ [y/N]: " CONFIRM
case "$CONFIRM" in
    [yY]|[yY][eE][sS]) ;;
    *) echo "キャンセルしました。"; exit 0 ;;
esac
echo ""

# ------------------------------------------------------------------------------
# STEP 4: 削除実行
# ------------------------------------------------------------------------------

for pattern in "${PATTERNS[@]}"; do
    while IFS= read -r target; do
        if [ -e "$target" ] || [ -L "$target" ]; then
            rm -rf "$target"
            echo "[削除完了] $target"
        fi
    done < <(find "$TARGET_DIR" -name "$pattern" 2>/dev/null)
done

# ------------------------------------------------------------------------------
# STEP 5: 結果確認
# ------------------------------------------------------------------------------
echo ""
echo "========================================"
echo " 処理完了"
echo "========================================"

# git 管理から外れたか確認
if git -C "$TARGET_DIR" rev-parse --git-dir > /dev/null 2>&1; then
    echo "[警告] まだ git 管理下と認識されています。手動で確認してください。"
else
    echo "[OK] git 管理情報がすべて削除されました。"
    echo "     普通のフォルダになっています。"
fi
echo "========================================"
